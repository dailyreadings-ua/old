const mixColors = function (colorOld, colorNew, percentage) {
    const resultObj = {
        r: 0,
        g: 0,
        b: 0,
    };

    const colors = {
        blanchedalmond: {
            r: 255,
            g: 235,
            b: 205,
        },
    
        darkslategrey: {
            r: 47,
            g: 79,
            b: 79,
        },
    
        darkseagreen: {
            r: 143,
            g: 188,
            b: 143,
        },
    
        lightcoral: {
            r: 240,
            g: 128,
            b: 128,
        },
    
        gold: {
            r: 255,
            g: 215,
            b: 0,
        },
    };

    ['r', 'g', 'b'].forEach(letter => {
        if (colors[colorOld][letter] == colors[colorNew][letter]) {
            resultObj[letter] = colors[colorOld][letter]
        } else {
            let average = Math.round(colors[colorOld][letter] + (colors[colorNew][letter] - colors[colorOld][letter])*percentage);
            let [min, max] = [0, 0];
            if (colors[colorOld][letter] > colors[colorNew][letter]) {
                max = colors[colorOld][letter];
                min = colors[colorNew][letter];
            } else {
                max = colors[colorNew][letter];
                min = colors[colorOld][letter];
            }
            if (average > max) {
                resultObj[letter] = max;
            } else if (average < min) {
                resultObj[letter] = min;
            } else {
                resultObj[letter] = average;
            }
        }
    
    });

    // resultObj.r = Math.round(colors[colorOld].r + (colors[colorNew].r - colors[colorOld].r)*percentage);
    // resultObj.g = Math.round(colors[colorOld].g + (colors[colorNew].g - colors[colorOld].g)*percentage);
    // resultObj.b = Math.round(colors[colorOld].b + (colors[colorNew].b - colors[colorOld].b)*percentage);

    // console.log(`rgb(${resultObj.r},${resultObj.g},${resultObj.b})`);
    return `rgb(${resultObj.r},${resultObj.g},${resultObj.b})`;
}

