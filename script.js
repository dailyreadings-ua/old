const pointsText = function(points) {
    let string = Math.abs(points).toString();
    let lastTwo = string.substring(string.length - 2);
    let lastOne = string.substring(string.length - 1);
    if (lastTwo == '11' || lastTwo == '12' || lastTwo == '13' || lastTwo == '14') {
        return 'баллов';
    } else if (lastOne == '1') {
        return 'балл';
    } else if (lastOne == '2' || lastOne == '3' || lastOne == '4') {
        return 'балла';
    } else {
        return 'баллов';
    }
}

