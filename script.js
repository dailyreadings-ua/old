    renderLesson: function(lesson) {
        mainMenu.transition = true;
        mainMenu.user.currentView = 'lesson';

        const self = this;
        const overlappingCont = document.querySelector('.overlappingCont');
        this.params.lastScrollPosition = overlappingCont.scrollTop;

        const selectedSectionTitle = document.querySelector('.selectedSectionTitle');
        const lessonsCont = document.querySelector('.lessonsCont');

        const needToScroll = self.params.lastScrollPosition > 0;

        if (needToScroll) {
            overlappingCont.scrollTo({top: 0, behavior: 'smooth'});
        }

        setTimeout(function() {
            lessonsCont.style.opacity = '0';
            selectedSectionTitle.style.opacity = '0';
        }, needToScroll * 300);

        setTimeout(function() {
            lessonsCont.remove();
            selectedSectionTitle.innerHTML = self.getLessonTitle(lesson);

            const vw = Math.max(document.documentElement.clientWidth || 0, window.innerWidth || 0);
            const allowedWidth = vw - ((vw / 10) + 25) + 1;
            const currentWidth = selectedSectionTitle.clientWidth;
            const currentFontSize = (vw / 10 + 5) / 1.2;

            const ratio = allowedWidth / currentWidth;

            if (ratio < 1) {
                selectedSectionTitle.style.fontSize = `${currentFontSize * ratio - 1}px`;
            }

            const lessonCont = document.createElement('div');
            lessonCont.classList.add('lessonCont')
            overlappingCont.appendChild(lessonCont);
    
            lesson.texts.forEach(function(textID) {
                const textEl = texts[lesson.chapter].find(el => el.id == textID);
                const textWrapper = document.createElement('div');
                textWrapper.setAttribute('textID', textEl.id);
                textWrapper.classList.add('textWrapper');
    
                const explCont = document.createElement('div');
                explCont.classList.add('explCont');
                textWrapper.appendChild(explCont);
                explCont.innerHTML = textEl.reason[0];
    
                const bookCont = document.createElement('div');
                bookCont.classList.add('bookCont');
                textWrapper.appendChild(bookCont);
                bookCont.innerHTML = textEl.book;
    
                const dotCont = document.createElement('div');
                dotCont.classList.add('filler');
                textWrapper.appendChild(dotCont);
                dotCont.innerHTML = '.';
    
                const chapterCont = document.createElement('div');
                chapterCont.classList.add('chapterCont');
                textWrapper.appendChild(chapterCont);
                chapterCont.innerHTML = textEl.chapter;
    
                const doubleDotCont = document.createElement('div');
                doubleDotCont.classList.add('filler');
                textWrapper.appendChild(doubleDotCont);
                doubleDotCont.innerHTML = ':';
    
                const verseCont = document.createElement('div');
                verseCont.classList.add('verseCont');
                textWrapper.appendChild(verseCont);
                verseCont.innerHTML = textEl.verse;
                textWrapper.appendChild(doubleDotCont.cloneNode(true));
    
                const textCont = document.createElement('div');
                textCont.classList.add('textCont2');
                textWrapper.appendChild(textCont);
                textCont.innerHTML = textEl.text;
    
                lessonCont.appendChild(textWrapper);
            });

            if (!self.selected.lessons.isStudying(lesson, 0)) {
                const studyButton = document.createElement('div');
                studyButton.classList.add('studyButton');
                studyButton.innerHTML = 'Изучать!';
    
                lessonCont.appendChild(studyButton);

                studyButton.addEventListener('click', function(){
					// debugger;
					const lessonObj = {
						info: {
							index: lessons.getLessonIndex(lesson, 0),
							repeatPhase: 0,
							nextRepeat: Date.now(),
							expiry: Date.now() + (6 * 60 * 60 * 1000),
							percentage: 0,
						},
						verses: {},
					};

					lesson.texts.forEach(function(textID) {
                        lessonObj.verses[`i_${textID}`] =
                            {
                                book: {
                                    done: false,
                                    score: 10,
                                },
                                chapter : {
                                    done: false,
                                    score: 10,
                                },
                                verse: {
                                    done: false,
                                    score: 10,
                                },
                                text: {
                                    done: false,
                                    score: 10,
                                },
                                expl: {
                                    done: false,
                                    score: 10,
                                },
                            };
                    });

                    self.selected.lessons.inProgress[0].push(lessonObj);
                    // let index = self.selected.lessons.inProgress[0].length - 1;
                    // self.selected.lessons.percentageStudied[0][index] = 0;

                    document.querySelector('.backCont').click();
                });
            }
    
            lessonCont.style.marginTop = `${selectedSectionTitle.offsetHeight + 15}px`;
            
            lessonCont.style.opacity = '1';
            selectedSectionTitle.style.opacity = '1';

            setTimeout(() => mainMenu.transition = false, 300);
        }, 300 + needToScroll * 300);
    },
